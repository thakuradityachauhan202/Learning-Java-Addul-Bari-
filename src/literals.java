import java.lang.*;
import java.util.*;
public class literals {
    public static void main(String args[])
    {
        byte b1=10;
        byte b2=0b1010;
        byte b3=012;
        byte b4=0xA;

        System.out.print(b1);
        System.out.print(b2);
        System.out.print(b3);
        System.out.print(b4);
    }
}
