import java.lang.*;
import java.lang.*;
public class methodpractice {
    static int max(int x,int y)
    {
        if(x>y)
            return x;
        else
            return y;
    }
    public static void main(String[] args)
    {
        int a=10,b=15;
        System.out.println(max(a,b));
    }
}
